'use strict';

/* Assigning New Values

  you can assign new values to a variable after they are declared.

  assignments happen from right to left!

  - the variable you want update is written on the LEFT of =
  - the new value is written on the RIGHT of =
*/
// declare a variable turtle and initialize it to the value 'Vinnie'
let turtle = 'Vinnie';
// assign a variable turtle and initialize it to the value 'Yurtle'
turtle = 'Yurtle';
// declare a variable food and initialize it to the value 'lettuce'
let food = 'lettuce';
// assign a variable food and initialize it to the value 'peanut'
food = 'peanut';
// declare a variable x and initialize it to the value 'abc'
let x = 'abc';
// assign a variable x and initialize it to the value '-|-'
x = '-|-';
