/* Declaring and Initializing

  you can also initialize a variable with a value
  this means that the variable will not be empty

  there are four main parts to declaring and initializing:

  - keyword: let, const or var.  (but just let for now)
  - name: a name to help you read the code
  - assignment operator (=)
  - initial value: what you want to store in the variable

*/

let turtle = 'Vinnie';
console.log(turtle);

let food = 'lettuce';
console.log(food);

let x = 'abc';
console.log(x);
